#include "funkcje.h"

double dodawanie(double arg1, double arg2)
{
    return arg1 + arg2;
}

double odejmowanie(double arg1, double arg2)
{
    return arg1 - arg2;
}

double mnozenie(double arg1, double arg2)
{
    return arg1 * arg2;
}

double dzielenie(double arg1, double arg2)
{
    return arg1 / arg2;
}
